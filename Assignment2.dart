id main() {
  // Integers
  int age = 20; // Example of an integer data type
  print('Age: $age');

  // Doubles
  double height = 1.85; // Example of a double data type
  print('Height: $height meters');

  // Strings
  String name = 'Mohas'; // Example of a string data type
  print('Name: $name');

  // Lists
  List<int> numbers = [1, 2, 3, 4, 5]; // Example of a list data type
  print('Numbers: $numbers');

  // Maps
  Map<String, dynamic> person = {
    'name': 'Mihra',
    'age': 18,
    'city': 'Arusha city'
  }; // Example of a map data type
  print('Person: $person');
}